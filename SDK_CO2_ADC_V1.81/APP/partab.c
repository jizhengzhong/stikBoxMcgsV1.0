/*****************************************************************************//*!
*
* @brief   Par Save  .
*     
* @param   pos : 0-PAR_POS_END
*
* @return  none
*
* @ Pass/ Fail criteria: none
*****************************************************************************/
void ParSave(uint16_t Pos)
{
    partab_type ParX;
    
    if(Pos >= PAR_POS_END)
        return;
    
    EEP_WP = 0;
    

    do {
        int32_t TimeOutCnt;    
        //写入数据
        Nw_eep_Send(0xA0, 2*Pos, ParTab[Pos].uBytes, 2);
        //读回校验
        ParX.uVal = ~ParTab[Pos].uVal;
        TimeOutCnt = 5;
        do {
            osDelay(8);
            if(IIC_ACK == Nw_eep_Rcv(0xA0, 2*Pos,ParX.uBytes, 2))
                break;
            TimeOutCnt--;
            if(TimeOutCnt < 0)
                break;
        } while (1);
        if(ParX.uVal == ParTab[Pos].uVal)
        //校验成功退出
            break;
    } while (1);
    
    EEP_WP = 1;
}

